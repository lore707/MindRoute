// Per-feature dictionaries extracted to ./i18n-dict to keep this file from
// growing without bound. Each uses a unique prefix (qa. qb. ql. acct. acin.
// res. and the misc namespaces cmp./fpm./rec./stream./nf.) so there are no
// collisions with the keys above; merged here so t() resolves them.
Object.assign(
  translations,
  quizADict,
  quizBDict,
  logisticsDict,
  accountDict,
  accountCinDict,
  residualDict,
  miscDict,
);

interface I18nContextType {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18nContextType>({
  lang: "en",
  setLang: () => {},
  t: (key: string) => key,
});

export function I18nProvider({ children }: { children: ReactNode }) {
 const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem("mindroute-lang");
    if (saved === "it" || saved === "en") return saved as Lang;
    return "en";
  });
  const setLang = useCallback((newLang: Lang) => {
    setLangState(newLang);
    localStorage.setItem("mindroute-lang", newLang);
  }, []);

  const t = useCallback((key: string) => {
    const entry = translations[key];
    if (!entry) return key;
    return entry[lang] || entry.en || key;
  }, [lang]);

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}









